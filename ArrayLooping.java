package com.belajar;

public class ArrayLooping {

    public static void main(String[] args) {

        String[] namaTeman = {"Rohman","Abdul","Hani","Ayyash","Faris","Hafsoh"};
        for (int i=0; i<namaTeman.length; i++) {
            System.out.println("Nama Teman Ke " +i+" : "+namaTeman[i]);

        }
    }
}
